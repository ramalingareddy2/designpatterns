package com.webaction.exception;

public class CommandConfigNotFoundException extends WebActionException {

	public CommandConfigNotFoundException() {
		super();
	}

	public CommandConfigNotFoundException(String message, Throwable cause) {
		super(message, cause);
	}

	public CommandConfigNotFoundException(String message) {
		super(message);
	}

	public CommandConfigNotFoundException(Throwable cause) {
		super(cause);
	}

}
